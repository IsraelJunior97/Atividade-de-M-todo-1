package variaveis;

public class Calculadora
{
    public  static void soma(double valor1,double valor2){

        double  resultado = valor1+valor2;

        System.out.println("Valor da soma de "+valor1 + " + "+valor2 +" Corresponde a "+resultado);


    }

    public  static void subtração(double valor1,double valor2){

        double  resultado = valor1-valor2;

        System.out.println("Valor da subtração de "+valor1 + " - "+ valor2 +" Corresponde a "+resultado);


    }

    public  static void multiplicação(double valor1,double valor2){

        double  resultado = valor1*valor2;

        System.out.println("Valor da multiplicacão de "+valor1 + " * "+valor2 +" Corresponde a "+resultado);


    }
    public  static void divisão(double valor1,double valor2){

        double  resultado = valor1/valor2;

        System.out.println("Valor da divisão de "+valor1 + " / "+valor2 +" Corresponde a "+resultado);


    }



}
